// 根据场景名称匹配SOP
function getSimplifiedSop(name, dept) {
  // 提取场景关键词
  for (const [keyword, sop] of Object.entries(sopMap)) {
    if (name.includes(keyword)) {
      return sop;
    }
  }
  
  // 默认SOP（根据部门）
  const defaultSopByDept = {
    '运营': ['采集业务数据', '执行自动化操作', '输出结果报告'],
    '财务': ['读取财务数据', '执行核算处理', '生成财务报表'],
    '供应链': ['采集供应链数据', '执行业务流程', '更新系统状态'],
    '客服': ['接收服务请求', '执行处理流程', '反馈处理结果'],
    '人事': ['采集人事数据', '执行人事流程', '生成人事报表'],
    '管理': ['汇总业务数据', '生成管理报表', '支持决策分析'],
    '合规': ['扫描合规数据', '识别风险问题', '输出合规报告'],
    '技术': ['执行技术任务', '验证执行结果', '记录操作日志']
  };
  
  return defaultSopByDept[dept] || ['执行自动化流程', '处理业务数据', '输出处理结果'];
}

