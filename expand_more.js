// 生成新场景
const scenarios = [...existing];
let id = existing.length + 1;

for (const dept of depts) {
  const templates = extraScenarios[dept] || [];
  for (const tpl of templates) {
    // 为每个模板生成多个平台版本
    const numPlatforms = Math.floor(Math.random() * 3) + 1; // 1-3个平台
    const selectedPlatforms = platforms.sort(() => Math.random() - 0.5).slice(0, numPlatforms);
    
    for (const platform of selectedPlatforms) {
      const model = models[Math.floor(Math.random() * models.length)];
      const type = types[Math.floor(Math.random() * types.length)];
      
      scenarios.push({
        name: `[${platform}] ${tpl.name}`,
        desc: tpl.desc,
        platform: platform,
        model: model,
        dept: dept,
        type: type,
        sop: tpl.sop
      });
      id++;
    }
  }
}

// 统计
const byDept = {};
const byPlat = {};
for (const s of scenarios) {
  byDept[s.dept] = (byDept[s.dept] || 0) + 1;
  byPlat[s.platform] = (byPlat[s.platform] || 0) + 1;
}

console.log('=== 场景扩展结果 ===');
console.log('原有场景:', existing.length);
console.log('新增场景:', scenarios.length - existing.length);
console.log('最终总数:', scenarios.length);

console.log('\n=== 按部门分布 ===');
Object.entries(byDept).sort((a,b) => b[1] - a[1]).forEach(([k,v]) => console.log(`  ${k}: ${v}`));

console.log('\n=== 按平台分布 ===');
Object.entries(byPlat).sort((a,b) => b[1] - a[1]).forEach(([k,v]) => console.log(`  ${k}: ${v}`));

